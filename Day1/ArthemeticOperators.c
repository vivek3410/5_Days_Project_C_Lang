#include<stdio.h>

int main(){
    float a = 10;
    float b = 20;

    int sum = a + b;
    int sub = a - b;
    int mul = a * b;
    float div = a / b;

    printf("Sum of a and b: %d\n",sum);
    printf("Sub of a and b: %d\n",sub);
    printf("multiplication of a and b: %d\n",mul);
    printf("division of a and b: %f\n",div);

}